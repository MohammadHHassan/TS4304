package day7.revision;

public class Revision
{
	public static void main(String[] args)
	{
		double a = 10.0;
		double b = 3.0;
		
		System.out.println(a+b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		System.out.println(10/3);
		System.out.println(10.0/3.0);
	}
}
